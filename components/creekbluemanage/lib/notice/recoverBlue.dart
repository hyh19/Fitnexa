  static sportGPS(HeadModel headModel,GPSModel locationModel){
    CreekTransport request = CreekTransport();
    protocol_exercise_gps_info_reply reply = protocol_exercise_gps_info_reply();
    reply.operate = operate_type.INQUIRE;
    reply.rmcLatitude = (double.parse(BlueTool().convertToDDMM(((locationModel.latitude ?? 0) / 1000000))) * 10000000).toInt() ;
    reply.rmcLongitude = (double.parse(BlueTool().convertToDDMM(((locationModel.longitude ?? 0) / 1000000))) * 10000000).toInt();

    reply.gsaPdop = locationModel.accuracy ?? 0;
    int gsaLocationType = 3;
    if ((locationModel.accuracy ?? 0) <= 2000 && (locationModel.accuracy ?? 0) != 0) {
      // GPS定位
      gsaLocationType = 3;
      if((locationModel.accuracy ?? 0) > 800){
        reply.gsaPdop = 800;
      }
    } else {
      // Wi-Fi定位或其他定位方式
      gsaLocationType = 1;
    }
    reply.gsaLocationType = gsaLocationType;
    CreekLog.info("回复gps：${locationModel.latitude},${locationModel.longitude}");
    DateTime dateTime = DateTime.now();
    String timeStr = dateTime.toString().substring(0,10);
    FileManage5.instance?.writeReceivedData("$timeStr.location","{\"rmc_latitude\":\"${reply.rmcLatitude}\",\"rmc_longitude\":\"${reply.rmcLongitude}\",\"gsa_pdop\":\"${reply.gsaPdop}\",\"rmc_location_status\":\"$gsaLocationType\"}");
    List<List<int>> listData = request.seedData(headModel, reply,recover: true);
    request.requestDevice(listData);
  }


